int main() {
  ios_base::sync_with_stdio(false), cin.tie(0);

  int n, k;
  cin >> n >> k;
  vector<int> v(n);
  for (int i = 0; i < n; ++i) {
    cin >> v[i];
  }

  int ans = 0;
  for (int i = n - 1; i >= 0; --i) {
    if (k < v[i]) {
      continue;
    }

    int cnt = k / v[i];

    k -= v[i] * cnt;
    ans += cnt;
  }

  cout << ans << endl;

  return 0;
}
